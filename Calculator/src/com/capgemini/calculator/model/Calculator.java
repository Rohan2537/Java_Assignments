package com.capgemini.calculator.model;

public class Calculator {

		public int AdditonOfTwoNumbers(int numberOne, int numberTwo){
			return numberOne+numberTwo;
			
		}
		
		public int SubtractionOfTwoNumbers(int numberOne, int numberTwo){
			return numberTwo-numberOne;
			
		}
		

		public int MultiplicationOfTwoNumbers(int numberOne, int numberTwo){
			return numberOne*numberTwo;
			
		}
		

		public int DivisionOfTwoNumbers(int numberOne, int numberTwo) {
			return numberOne/numberTwo;

			
		}
}
